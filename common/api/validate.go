import (
	"github.com/asaskevich/govalidator"
	"log"
	"net/http"
)

type validate interface {
	Message() map[string]int
}

func (g *Gin) Validate(obj validate) bool {
	b, err := govalidator.ValidateStruct(obj)
	if err != nil {
		log.Println(err)
		g.Response(http.StatusOK, 400000000, nil)
	}

	return b
}
